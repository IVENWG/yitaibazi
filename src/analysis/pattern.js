/**
 * 格局分类主函数 —— 对应 a0.cs c_Method()
 * @param {Object} pillars  四柱数据
 * @returns {string} 格局名称
 */
function classifyPattern(pillars) {
  const key = pillars.day.stem + pillars.month.branch;
  const entry = PT[key];
  if (!entry) return '普通格局';
  if (typeof entry === 'string') return entry;
  const o = countStems(pillars);
  return entry(o) || '普通格局';
}

